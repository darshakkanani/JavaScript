let a = 5;
let b = 2;

// Arithmetic Operators

console.log("a = 5 & b = 2");
console.log("a + b = ",a + b);
console.log("a - b = ",a - b);
console.log("a * b = ",a * b);
console.log("a / b = ",a / b);
console.log("a ** b = ",a ** b);
console.log("a % b = ",a % b);

// Unary Operators

console.log("a++ = ",a++);
console.log("After a = ",a);
console.log("a-- = ",a--);
console.log("After a = ",a);
console.log("++a = ",++a);
console.log("--a = ",--a);

// Asignment Operators

console.log("a += 5 = ",a += 5);
console.log("a -= 5 = ",a -= 5);
console.log("a *= 5 = ",a *= 5);
console.log("a /= 5 = ",a /= 5);
console.log("a **= 5 = ",a **= 5);
console.log("a %= 5 = ",a %= 5);


